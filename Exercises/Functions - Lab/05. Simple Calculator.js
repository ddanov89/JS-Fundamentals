function simpleCalculator(a, b, operator) {

    if (operator == "add") {
        console.log(a + b);
    }else if (operator == "subtract") {
        console.log(a - b);
    }else if ( operator == "divide"){
        console.log(a / b);
    }else if( operator == "multiply"){
        console.log(a * b);
    }
}