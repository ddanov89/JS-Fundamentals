function getLetter(s) {
    let letter;
    // Write your code here
    switch (s){
    case 1: if(s = {a,e,i,o,u})
    {letter = "A"}; break;
    case 2: if(s = {b,c,d,f,g})
    {letter = "B"
    }; break;
    case 3: if(s = {h,j,k,l,m})
    {letter = "C"
    }; break;
    case 4: if(s = {n,p,q,r,s,t,v,w,x,y,z})
    {letter = "D"
    }; break;
    }
    return letter;
}
